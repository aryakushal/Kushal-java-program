package pack.com;

public class preincrementpostincrement {

	public static void main(String[] args) {
		int i,k;
		i=6;
		k=i++;//post increment
		System.out.println("k="+k);
		System.out.println("i="+i);
		k=++i;//pre incremennt
		System.out.println("k="+k);
		System.out.println("i="+i);
		
		
		
		
		

	}

}
